// Você também pode omitir todos os três blocos. 
//Novamente, certifique-se de usar uma instrução break no final do loop e também modificar 
// (incrementar) uma variável, para que a condição do break seja verdadeira em algum momento.

var i = 0;

for (;;) {
    if (i > 3) break;
    console.log(i);
    i++;
}